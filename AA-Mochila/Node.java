public class Node {
    private int level;
    private int profit;
    private int bound;
    private int peso;

    public Node() { 
    }

    public int getLevel() {
        return level;
    }

    public int getProfit() {
        return profit;
    }

    public int getBound() {
        return bound;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public void setProfit(int profit) {
        this.profit = profit;
    }

    public void setBound(int bound) {
        this.bound = bound;
    }

    public void setLevel(int level) {
        this.level = level;
    }          
    
}








